import { buildApp } from "../src/app.js";
import { getMagika } from "../src/services/magika.js";

let app = null;

export async function getApp() {
  if (!app) {
    await getMagika();
    app = buildApp();
    await app.ready();
  }
  return app;
}

export async function closeApp() {
  if (app) {
    await app.close();
    app = null;
  }
}

